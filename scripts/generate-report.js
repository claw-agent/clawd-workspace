async function generateReport() {
  // Ensure output directory exists
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }

  // Get all markdown files
  const files = fs.readdirSync(RESEARCH_DIR)
    .filter(f => f.endsWith('.md'))
    .sort();

  if (files.length === 0) {
    console.error('No research files found!');
    process.exit(1);
  }

  console.log(`Found ${files.length} research files`);

  // Build table of contents
  const toc = files.map((f, i) => {
    const name = f.replace('.md', '').replace(/-/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
    return `<li><span class="emoji">üìÑ</span> ${i + 1}. ${name}</li>`;
  }).join('\n');

  // Build sections
  const sections = files.map((f, i) => {
    const content = fs.readFileSync(path.join(RESEARCH_DIR, f), 'utf-8');
    const name = f.replace('.md', '').replace(/-/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
    
    // Convert markdown to HTML (basic conversion)
    let html = content
      // Headers
      .replace(/^### (.*$)/gm, '<h3>$1</h3>')
      .replace(/^## (.*$)/gm, '<h2>$1</h2>')
      .replace(/^# (.*$)/gm, '<h1>$1</h1>')
      // Bold and italic
      .replace(/\*\*\*(.*?)\*\*\*/g, '<strong><em>$1</em></strong>')
      .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
      .replace(/\*(.*?)\*/g, '<em>$1</em>')
      // Code blocks
      .replace(/```(\w*)\n([\s\S]*?)```/g, '<pre><code>$2</code></pre>')
      .replace(/`([^`]+)`/g, '<code>$1</code>')
      // Lists
      .replace(/^\s*[-*]\s+(.*)$/gm, '<li>$1</li>')
      .replace(/(<li>.*<\/li>\n?)+/g, '<ul>$&</ul>')
      // Numbered lists  
      .replace(/^\d+\.\s+(.*)$/gm, '<li>$1</li>')
      // Links
      .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>')
      // Paragraphs
      .replace(/\n\n/g, '</p><p>')
      // Highlight boxes for key findings
      .replace(/<p><strong>Key (.*?):<\/strong>/gi, '<div class="highlight-box"><strong>Key $1:</strong>')
      .replace(/<p><strong>Recommendation.*?:<\/strong>/gi, '<div class="action-item"><strong>Recommendation:</strong>');

    return `
      <div class="section">
        <div class="section-header">
          <h2 style="margin:0;color:white;">üìå ${i + 1}. ${name}</h2>
        </div>
        <div class="section-content">
          ${html}
        </div>
      </div>
    `;
  }).join('\n');

  // Build cover
  const date = new Date().toLocaleDateString('en-US', { 
    weekday: 'long', 
    year: 'numeric', 
    month: 'long', 
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
    timeZone: 'America/Denver'
  });

  const cover = `
    <div class="cover">
      <h1>üåÖ Morning Research Report</h1>
      <div class="date">${date}</div>
      <div class="subtitle">${files.length} Deep-Dive Research Documents</div>
    </div>
    <div class="toc">
      <h2>üìë Table of Contents</h2>
      <ul>${toc}</ul>
    </div>
  `;

  // Assemble final HTML
  const html = TEMPLATE
    .replace('{{CONTENT}}', cover + sections)
    .replace('{{DATE}}', date);

  // Write HTML
  const htmlPath = path.join(OUTPUT_DIR, 'morning-report.html');
  fs.writeFileSync(htmlPath, html);
  console.log(`HTML saved to ${htmlPath}`);

  // Generate PDF using Puppeteer
  try {
    const puppeteer = require('puppeteer');
    const browser = await puppeteer.launch({ headless: true });
    const page = await browser.newPage();
    await page.setContent(html, { waitUntil: 'networkidle0' });
    
    const pdfPath = path.join(OUTPUT_DIR, `morning-report-${new Date().toISOString().split('T')[0]}.pdf`);
    await page.pdf({
      path: pdfPath,
      format: 'A4',
      margin: { top: '20mm', bottom: '20mm', left: '15mm', right: '15mm' },
      printBackground: true
    });
    
    await browser.close();
    console.log(`PDF saved to ${pdfPath}`);
    return pdfPath;
  } catch (err) {
    console.error('Puppeteer PDF failed, trying pandoc...', err.message);
    
    // Fallback to pandoc
    const mdPath = path.join(OUTPUT_DIR, 'morning-report.md');
    const combinedMd = files.map(f => {
      const content = fs.readFileSync(path.join(RESEARCH_DIR, f), 'utf-8');
      const name = f.replace('.md', '').replace(/-/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
      return `# ${name}\n\n${content}\n\n---\n\n`;
    }).join('');
    
    fs.writeFileSync(mdPath, `# Morning Research Report\n\n${date}\n\n---\n\n${combinedMd}`);
    
    const pdfPath = path.join(OUTPUT_DIR, `morning-report-${new Date().toISOString().split('T')[0]}.pdf`);
    execSync(`pandoc "${mdPath}" -o "${pdfPath}" --pdf-engine=wkhtmltopdf 2>/dev/null || pandoc "${mdPath}" -o "${pdfPath}" 2>/dev/null`);
    console.log(`PDF saved to ${pdfPath}`);
    return pdfPath;
  }
}

generateReport().then(pdfPath => {
  console.log('Report generation complete!');
  console.log('PDF:', pdfPath);
}).catch(err => {
  console.error('Error generating report:', err);
  process.exit(1);
});
